package Session_3.Homework.Homework3;

public class Exercitiul_4 {
    public static void main(String[] args) {

        int i;

        System.out.println("Numaratoarea ");

        for (i = 1; i <= 10; i++) {
            System.out.println(i + " ");
        }

        System.out.println("Numaratoare inversa ");
        for (i = 10; i >= 1; i--) {
            System.out.println(i + " ");
        }
    }
}

