package Session_4.Homework4;

public class Challenge_16 {
    public static void main(String[] args) {
        // Initialize number to 50
        int number = 50;


        number = -number;
        System.out.println("After negating: " + number);


        number = Math.abs(number);
        System.out.println("After taking absolute value: " + number);


        number++;
        System.out.println("After incrementing by 1: " + number);
    }
}
