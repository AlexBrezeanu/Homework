package Session_4.Homework4;

public class Challenge_5 {
    public static void main(String[] args) {
        System.out.println("Numbers from 1 to 10:");
        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " ");
        }
        System.out.println(" ");


        System.out.println("Even numbers from 1 to 10:");
        for (int i = 2; i <= 10; i += 2) {
            System.out.print(i + " ");
        }

    }
}
